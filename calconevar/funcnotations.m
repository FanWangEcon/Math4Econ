%% Function Notations
% When you read your first economic papers, perhaps you are confused about function 
% notation. Economic models include potentially a lot of variables, and different 
% functional notations are used by different authors based on personal preference. 
% It's a choice you get to make, just make sure you use consistent notations, 
% and state clearly what your notations mean.
% 
% In general, the letter $f$ represents a particular rule of mapping, if 
% there are multiple functional relationships in an economic model, different 
% letters could be used, for example:
% 
% $$y = f(h), l = g(h), u = w(h)$$
% 
% These three equations could be from a model where:
% 
% * $h$ is the number of hours an individual works
% * $y$ is the income that the individual makes as a function of work hours
% * $l$ is the number of leisure hours remaining as a function work hours
% * $u$ is the overall level of utility as a function of work hours.
% 
% Sometimes we have to write down many functions, and we can potentially 
% run out of letters. Hence, we sometimes use the a common letter for function 
% but different subscripts:
% 
% $$y = f_y(h), l = f_l(h), u = f_w(h)$$
% 
% or we could use the same letter for function as the letter for the value 
% of the function
% 
% $$y = y(h), l = l(h), u = u(h)$$
% 
% if the above notation seems unclear, you can perhaps use more cursive letters 
% for functions
% 
% $$y = \mathcal{y}(h), l = \mathcal{l}(h), u = \mathcal{u}(h)$$
% 
% Again, make sure you are clear about what your notations mean. Math should 
% help make our econ ideas more clear to ourselves and others, and that starts 
% with clear functional notations.
% 
